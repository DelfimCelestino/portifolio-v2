export const useTheme = () => {
  const [themeColor, setThemeColor] = useState<ThemeColor>("green");
  const { theme, setTheme } = useNextTheme();

  useEffect(() => {
    const savedThemeColor = localStorage.getItem("themeColor") as ThemeColor;
    const savedTheme = localStorage.getItem("theme") as ThemeMode | null;

    if (savedThemeColor && themes[savedThemeColor]) {
      setThemeColor(savedThemeColor);
    }

    if (savedTheme) {
      setTheme(savedTheme);
    }
  }, [setTheme]);

  useEffect(() => {
    const root = document.documentElement;
    const isDark = theme === "dark";
    const currentTheme = themes[themeColor];

    Object.entries(currentTheme[isDark ? "dark" : "light"]).forEach(
      ([key, value]) => {
        root.style.setProperty(`--${key}`, value);
      }
    );
  }, [themeColor, theme]);

  const updateTheme = (newThemeColor: ThemeColor) => {
    setThemeColor(newThemeColor);
    localStorage.setItem("themeColor", newThemeColor);
  };

  const updateMode = (newTheme: ThemeMode) => {
    setTheme(newTheme);
    localStorage.setItem("theme", newTheme);
  };

  return {
    themeColor,
    setThemeColor: updateTheme,
    theme,
    setTheme: updateMode,
  };
};
